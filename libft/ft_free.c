#include "libft.h"

void	*ft_free(char **ptr)
{
	int	i;

	i = -1;
	while (ptr[++i] != NULL)
	{
		free(ptr[i]);
		ptr[i] = NULL;
	}
	free(ptr);
	ptr = NULL;
	return (NULL);
}
